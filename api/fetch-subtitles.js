// Strategia unica affidabile: InnerTube Android client API
// - Bypassa il consent wall GDPR (nessun cookie richiesto)
// - Non bloccata dagli IP datacenter come le richieste web
// - Restituisce caption tracks con signed baseUrl pronti all'uso
// - Funziona su tutti i TED Talk che hanno sottotitoli

export const config = { maxDuration: 30 };

// ── helpers ──────────────────────────────────────────────────────────────────

function extractVideoId(input) {
    const patterns = [
        /(?:youtube\.com\/watch\?v=|youtu\.be\/|youtube\.com\/embed\/)([a-zA-Z0-9_-]{11})/,
        /^([a-zA-Z0-9_-]{11})$/
    ];
    for (const p of patterns) {
        const m = input?.match(p);
        if (m) return m[1];
    }
    return null;
}

function decodeUrl(url) {
    return url.replace(/\\u0026/g, '&').replace(/\\u0027/g, "'");
}

function xmlToItems(xml) {
    const items = [];
    const re = /<text[^>]*\bstart="([^"]+)"[^>]*>([\s\S]*?)<\/text>/g;
    let m;
    while ((m = re.exec(xml)) !== null) {
        const offset = Math.round(parseFloat(m[1]) * 1000);
        const text = m[2]
            .replace(/<[^>]+>/g, '')
            .replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>')
            .replace(/&#39;/g, "'").replace(/&apos;/g, "'").replace(/&quot;/g, '"')
            .replace(/\n/g, ' ').trim();
        if (text) items.push({ text, offset });
    }
    return items;
}

function formatWithTimestamps(items) {
    return items.map(({ text, offset }) => {
        const s = Math.floor((offset ?? 0) / 1000);
        const mm = String(Math.floor(s / 60)).padStart(2, '0');
        const ss = String(s % 60).padStart(2, '0');
        return `[${mm}:${ss}] ${text.trim()}`;
    }).filter(l => l.length > 8).join('\n');
}

// ── InnerTube Android client ──────────────────────────────────────────────────
// Simula l'app YouTube Android — bypass consent GDPR e datacenter blocking.
// Chiamata al player endpoint → ottieni captionTracks con signed baseUrl.

async function fetchCaptionsViaAndroid(videoId) {
    const body = {
        videoId,
        context: {
            client: {
                clientName: 'ANDROID',
                clientVersion: '19.09.37',
                androidSdkVersion: 30,
                hl: 'en',
                gl: 'US',
                timeZone: 'UTC',
                utcOffsetMinutes: 0,
                userAgent: 'com.google.android.youtube/19.09.37 (Linux; U; Android 11) gzip'
            }
        }
    };

    const res = await fetch(
        'https://www.youtube.com/youtubei/v1/player?prettyPrint=false',
        {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'User-Agent': 'com.google.android.youtube/19.09.37 (Linux; U; Android 11) gzip',
                'X-Youtube-Client-Name': '3',
                'X-Youtube-Client-Version': '19.09.37',
                'Accept-Language': 'en-US,en;q=0.9',
                'Origin': 'https://www.youtube.com'
            },
            body: JSON.stringify(body)
        }
    );

    if (!res.ok) throw new Error(`InnerTube Android: HTTP ${res.status}`);
    const data = await res.json();

    // Verifica che il video esista e non sia privato
    const status = data?.playabilityStatus?.status;
    if (status && status !== 'OK') {
        const reason = data?.playabilityStatus?.reason || status;
        throw new Error(`Video non disponibile: ${reason}`);
    }

    const captionTracks = data?.captions?.playerCaptionsTracklistRenderer?.captionTracks;
    if (!captionTracks?.length) throw new Error('Nessuna traccia sottotitoli disponibile per questo video');

    // Scegli la traccia migliore:
    // 1. en nativo (non auto-generated)  2. en qualsiasi  3. prima disponibile
    const track =
        captionTracks.find(t => t.languageCode?.startsWith('en') && !t.kind) ||
        captionTracks.find(t => t.languageCode?.startsWith('en')) ||
        captionTracks[0];

    const trackUrl = decodeUrl(track.baseUrl || '');
    if (!trackUrl) throw new Error('baseUrl mancante nella traccia selezionata');

    // Aggiunge fmt=srv3 per ottenere XML con timestamp
    const finalUrl = trackUrl.includes('fmt=') ? trackUrl : `${trackUrl}&fmt=srv3`;

    const subRes = await fetch(finalUrl, {
        headers: {
            'User-Agent': 'com.google.android.youtube/19.09.37 (Linux; U; Android 11) gzip'
        }
    });
    if (!subRes.ok) throw new Error(`Download sottotitoli: HTTP ${subRes.status}`);

    const xml = await subRes.text();
    const items = xmlToItems(xml);
    if (!items.length) throw new Error('XML sottotitoli vuoto o formato non riconosciuto');

    return {
        items,
        lang: track.languageCode,
        trackName: track.name?.simpleText || track.languageCode,
        isAutoGenerated: track.kind === 'asr'
    };
}

// ── Handler principale ────────────────────────────────────────────────────────

export default async function handler(req, res) {
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    if (req.method === 'OPTIONS') return res.status(200).end();

    const videoId = extractVideoId(req.query.videoId);
    if (!videoId) return res.status(400).json({ message: 'videoId non valido o mancante' });

    try {
        const { items, lang, trackName, isAutoGenerated } = await fetchCaptionsViaAndroid(videoId);
        return res.status(200).json({
            subtitles: formatWithTimestamps(items),
            source: 'innertube-android',
            lang,
            trackName,
            isAutoGenerated,
            itemCount: items.length
        });
    } catch (err) {
        console.error(`[fetch-subtitles] Failed for ${videoId}: ${err.message}`);
        return res.status(502).json({
            message: err.message,
            videoId
        });
    }
}
